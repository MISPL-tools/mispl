let currentLang = 'nl'; // Fallback

function setLanguage(langCode) {
	if (dictionary[langCode]) {
		currentLang = langCode;
	} else {
		currentLang = 'en'; // Als iemand bijv. Spaans heeft, val terug op Engels
	}
}

function t(code, ...args) {
	// Zoek de tekst in de huidige taal, of val terug op Engels, of print de ruwe code
	let msg = dictionary[currentLang][code] || dictionary['en'][code] || code;

	// Vervang {0}, {1} etc. met dynamische variabelen (zoals de naam van de MISPL variabele)
	args.forEach((arg, index) => {
		msg = msg.replace(`{${index}}`, arg);
	});
	return msg;
}

// Een hulpfunctie om het woordenboek later makkelijk uit te breiden zonder 1 enorm bestand te maken
function addTranslations(newTranslations) {
	for (const lang in newTranslations) {
		if (dictionary[lang]) {
			dictionary[lang] = { ...dictionary[lang], ...newTranslations[lang] };
		}
	}
}

module.exports = { setLanguage, t, addTranslations };